class Assignment {
    public static void main(String[] args) {
        int a;
        a=10; //Simple
        System.out.println(a);
        
        a+=10; //Compound
        System.out.println(a);
        
        a-=10; //Compound
        System.out.println(a);

        a*=10; //Compound
        System.out.println(a);

        a/=10; //Compound
        System.out.println(a);
    }
}
