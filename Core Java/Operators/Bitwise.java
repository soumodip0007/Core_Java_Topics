class Bitwise {
    public static void main(String[] args) {
        int a=5, b=7;

        System.out.println("AND = " +(a & b)); //AND
        System.out.println("OR = " +(a | b)); //OR
        System.out.println("XOR = " +(a ^ b)); //XOR
        System.out.println("Complement = " +(~b)); //Complement
    }
}