class PrePost {
    public static void main(String[] args) {
        int a = 10;
        System.out.println(a);
        System.out.println("Pre & Post - Increment");
        System.out.println(a++);
        System.out.println(++a);

        System.out.println(a);
        System.out.println("Pre & Post - Deccrement");
        System.out.println(a--);
        System.out.println(--a);

    }
}
